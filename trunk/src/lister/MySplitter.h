#ifndef _MySplitter_h_
#define _MySplitter_h_

#include "shared.h"

//==========================================================================================	
class MySplitter: public Splitter {
	public:

	//==========================================================================================	
	MySplitter() : Splitter() {
	}

	//==========================================================================================	
	void Xmlize(XmlIO xml) {
		int a;
		
		if(xml.IsLoading()) {
			xml("pos", a);
			if (a == 0) a = 20;
			SetPos(a);
		} else {
			// Storing
			a = GetPos();
			xml("pos", a);
		}
	}
	
	//==========================================================================================	
};

#endif
